package com.zx.pojo;

public class Log {
	private int id;
	private int outUno;
	private int inUno;
	private double money;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getOutUno() {
		return outUno;
	}
	public void setOutUno(int outUno) {
		this.outUno = outUno;
	}
	public int getInUno() {
		return inUno;
	}
	public void setInUno(int inUno) {
		this.inUno = inUno;
	}
	public double getMoney() {
		return money;
	}
	public void setMoney(double money) {
		this.money = money;
	}
	
}
